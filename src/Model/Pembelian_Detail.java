public class Pembelian_Detail {
    private String No_Masuk;
    private String Kode_Barang;
    private Integer Harga_Beli;
    private Integer Jumlah;
    private Integer integer;

    public String getNo_Masuk() {
        return No_Masuk;
    }

    public void setNo_Masuk(String No_Masuk) {
        this.No_Masuk = No_Masuk;
    }

    public String getKode_Barang() {
        return Kode_Barang;
    }

    public void setKode_Barang(String Kode_Barang) {
        this.Kode_Barang = Kode_Barang;
    }

    public Integer getHarga_Beli() {
        return Harga_Beli;
    }

    public void setHarga_Beli(Integer Harga_Beli) {
        this.Harga_Beli = Harga_Beli;
    }

    public Integer getJumlah() {
        return Jumlah;
    }

    public void setJumlah(Integer Jumlah) {
        this.Jumlah = Jumlah;
    }

    public Integer getInteger() {
        return integer;
    }

    public void setInteger(Integer integer) {
        this.integer = integer;
    }
    
    
}
